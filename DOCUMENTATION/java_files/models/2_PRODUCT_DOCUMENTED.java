    /**
     * Filter products list by seller ID
     * 
     * Returns new list containing only products sold by specified seller.
     * Useful for displaying "all items from seller" views.
     * 
     * Example usage:
     * List<Product> sellerProducts = Product.filterBySeller(allProducts, "userId123");
     * 
     * @param allProducts List of all products to filter
     * @param sellerId Seller ID to filter by
     * @return New list containing only products from specified seller
     */
    public static List<Product> filterBySeller(List<Product> allProducts, String sellerId) {
        // Create empty result list
        List<Product> result = new ArrayList<>();
        
        // If either parameter is null, return empty list
        if (allProducts == null || sellerId == null)
            return result;
        
        // Loop through all products
        for (Product p : allProducts) {
            // If product's seller matches desired seller, add to result
            if (sellerId.equals(p.getSellerId()))
                result.add(p);
        }
        
        // Return filtered list
        return result;
    }

