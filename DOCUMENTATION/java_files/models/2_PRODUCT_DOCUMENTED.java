/*
 * ============================================================================
 * FILE: Product.java
 * PURPOSE: Data model representing a product listing in the UniTrade marketplace
 * LOCATION: app/src/main/java/com/example/unitrade/Product.java
 * 
 * DESCRIPTION:
 * The Product class is the core data model for all product listings in the
 * marketplace. It stores comprehensive information about items for sale,
 * including descriptions, pricing, images, and transaction history. Products
 * can have different statuses (SOLD, BOUGHT, DONATED) representing their
 * state in the marketplace lifecycle. The class implements Parcelable for
 * efficient inter-Activity data passing.
 * 
 * KEY FEATURES:
 * - Product details (name, price, description, condition)
 * - Multiple product images with URLs
 * - Category and location information
 * - Seller and buyer tracking
 * - Transaction status tracking
 * - Image version control (cache busting)
 * - Listing and transaction timestamps
 * - QR code for payment tracking
 * - Utility methods for filtering and searching
 * 
 * STATUS CONSTANTS:
 * - STATUS_BOUGHT = "BOUGHT"   : User purchased this product
 * - STATUS_SOLD = "SOLD"       : User sold this product
 * - STATUS_DONATED = "DONATED" : Product was donated
 * 
 * ============================================================================
 */

