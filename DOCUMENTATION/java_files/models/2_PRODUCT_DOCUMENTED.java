    /**
     * Constructor for deserialization from Parcel
     * Reconstructs Product from binary Parcel data when passed between Activities
     * @param in Parcel containing serialized Product
     */
    protected Product(Parcel in) {
        // Read strings (in same order as writeToParcel)
        productId = in.readString();           // Read product ID
        productName = in.readString();         // Read product name
        productPrice = in.readDouble();        // Read price
        imageUrls = in.createStringArrayList(); // Read image URLs list
        productDescription = in.readString(); // Read description
        productCondition = in.readString();   // Read condition
        productUsedDaysTotal = in.readInt(); // Read days used
        productStatus = in.readString();     // Read status
        productCategory = in.readString();   // Read category
        location = in.readString();          // Read location
        sellerId = in.readString();          // Read seller ID
        buyerId = in.readString();           // Read buyer ID
        
        // Read boolean as byte (0 or 1)
        isHeader = in.readByte() != 0;      // Read header flag
        
        // Read more strings
        qrPaymentUrl = in.readString();     // Read QR code URL
        
        // Read long value
        imageVersion = in.readLong();       // Read image version
        
        // Read listing date (stored as long timestamp)
        long time = in.readLong();
        listingDate = time == -1 ? null : new Date(time);  // Convert back to Date
        
        // Read transaction date
        transactionDate = in.readLong();    // Read transaction timestamp
        
        // Read transaction info
        soldTo = in.readString();           // Read sold to user ID
        long soldAtTime = in.readLong();
        soldAt = soldAtTime == -1 ? null : new Timestamp(new Date(soldAtTime)); // Convert to Timestamp
        purchaseId = in.readString();       // Read purchase ID
        
        // Read transaction flags
        buyTransaction = in.readByte() != 0;    // Read buy flag
        sellTransaction = in.readByte() != 0;   // Read sell flag
        donation = in.readByte() != 0;          // Read donation flag
        
        // Read stability
        stability = in.readInt();           // Read stability score
    }

    /**
     * Serialize Product to Parcel for passing between Activities
     * 
     * IMPORTANT: Write order must match constructor read order!
     * 
     * @param dest Parcel to write to
     * @param flags Parcelable flags (usually 0)
     */
    @Override
    public void writeToParcel(Parcel dest, int flags) {
        // Write strings in consistent order
        dest.writeString(productId);         // Write product ID
        dest.writeString(productName);       // Write product name
        dest.writeDouble(productPrice);      // Write price
        dest.writeStringList(imageUrls);     // Write image URLs
        dest.writeString(productDescription); // Write description
        dest.writeString(productCondition);  // Write condition
        dest.writeInt(productUsedDaysTotal); // Write days used
        dest.writeString(productStatus);     // Write status
        dest.writeString(productCategory);   // Write category
        dest.writeString(location);          // Write location
        dest.writeString(sellerId);          // Write seller ID
        dest.writeString(buyerId);           // Write buyer ID
        
        // Write boolean as byte (1 for true, 0 for false)
        dest.writeByte((byte) (isHeader ? 1 : 0)); // Write header flag
        
        // Write more strings
        dest.writeString(qrPaymentUrl);      // Write QR code URL
        
        // Write long
        dest.writeLong(imageVersion);        // Write image version
        
        // Write listing date as long (timestamp)
        dest.writeLong(listingDate != null ? listingDate.getTime() : -1);
        
        // Write transaction date
        dest.writeLong(transactionDate);     // Write transaction timestamp
        
        // Write transaction info
        dest.writeString(soldTo);            // Write sold to user ID
        // Write Timestamp as long (represent as timestamp or -1 if null)
        dest.writeLong(soldAt != null ? soldAt.toDate().getTime() : -1);
        dest.writeString(purchaseId);        // Write purchase ID
        
        // Write transaction flags as bytes
        dest.writeByte((byte) (buyTransaction ? 1 : 0));  // Write buy flag
        dest.writeByte((byte) (sellTransaction ? 1 : 0)); // Write sell flag
        dest.writeByte((byte) (donation ? 1 : 0));        // Write donation flag
        
        // Write stability
        dest.writeInt(stability);            // Write stability score
    }

