    /**
     * Check if two Product objects are equal
     * Based on product ID comparison (two products are equal if same ID)
     * @param o Object to compare
     * @return True if products have same ID
     */
    @Override
    public boolean equals(Object o) {
        // If comparing to self, return true
        if (this == o)
            return true;
        
        // If not a Product instance, return false
        if (!(o instanceof Product))
            return false;
        
        // Cast to Product and compare IDs
        Product p = (Product) o;
        return productId != null && productId.equals(p.productId);
    }

